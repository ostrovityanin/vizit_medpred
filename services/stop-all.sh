#!/bin/bash

# Скрипт для остановки всех микросервисов проекта

echo "Остановка всех микросервисов..."

# Определяем базовую директорию
BASE_DIR="$(dirname "$0")"
cd "$BASE_DIR"

# Останавливаем GPT-4o Audio Preview микросервис, если он существует
if [ -d "gpt4o-audio-service" ]; then
  echo "Останавливаем GPT-4o Audio микросервис..."
  cd gpt4o-audio-service
  ./stop.sh
  cd ..
  echo "GPT-4o Audio микросервис остановлен"
fi

# Здесь можно добавить остановку других микросервисов

echo "Все микросервисы остановлены"