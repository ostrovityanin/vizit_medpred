#!/bin/bash

# Скрипт для запуска всех микросервисов

echo "Запуск всех микросервисов..."

# Переходим в основную директорию сервисов
cd "$(dirname "$0")"

# Запускаем мониторинг
echo "Запуск сервиса мониторинга..."
./monitoring/start-monitoring.sh

# Ждем немного для инициализации мониторинга
sleep 2

# Запускаем сервис отправки аудио в Telegram
echo "Запуск сервиса отправки аудио в Telegram..."
./audio-notifier/start.sh

echo "Все микросервисы запущены."